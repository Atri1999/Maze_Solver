from PIL import Image as im
import numpy as np 

def clean(arr):
    x=len(arr)
    y=len(arr[0])
    for i in range(x):
        for j in range(y):
            if arr[i][j]==1:
                arr[i][j]=255
    return arr

def maze(arr):
    arr_=np.array(arr,np.uint8)

    #print(arr_)

    img=im.fromarray(arr_)
    img.save("demo_maze4.jpg")


